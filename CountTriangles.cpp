int solution(vector<int> &A) {
    // Implement your solution here

    sort(A.begin(), A.end());

    int cnt {0};
    int n = A.size();
    for (int p{0}; p < n - 2; ++p){

        int r = p + 2;
        for (int q = p + 1; q < n - 1; ++q) {

            while (r < n && A[p] + A[q] > A[r]) { r++; }

            cnt += (r - q) - 1;
        }
    }

    return cnt;
}